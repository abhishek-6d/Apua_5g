package com.sixdee.imp.dto;

public class featureMappingDTO {
	int id;
	String featureId;
	String blClassPath;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getFeatureId() {
		return featureId;
	}

	public void setFeatureId(String featureId) {
		this.featureId = featureId;
	}

	public String getBlClassPath() {
		return blClassPath;
	}

	public void setBlClassPath(String blClassPath) {
		this.blClassPath = blClassPath;
	}

}
