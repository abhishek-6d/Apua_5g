public class ReResponseParameter {

	private String id = null;
	private String value = null;
	public ReResponseParameter(String id, String value) {
		this.id = id;
		this.value = value;
	}
	public ReResponseParameter() {
		// TODO Auto-generated constructor stub
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	
}
